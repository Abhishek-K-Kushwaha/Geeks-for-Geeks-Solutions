class Solution
{
public:
    vector<long long> factorialNumbers(long long N)
    {
        long long num = 1;
        long long count = 1;
        vector <long long> lst;
        while (true){
            if (num > N) break;
            lst.push_back(num);
            count++;
            num = num*count;
            }
        return lst;
    }
};

