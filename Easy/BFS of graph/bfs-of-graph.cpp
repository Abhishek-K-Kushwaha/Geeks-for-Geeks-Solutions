class Solution {
  public:
    // Function to return Breadth First Traversal of given graph.
    vector<int> bfsOfGraph(int V, vector<int> adj[]) {
        queue<int> q;
        vector<int> ans;
        vector<int> visited(V);
        q.push(0);
        visited[0] = 1;
        while(!q.empty()){
            int ele = q.front();
            ans.push_back(ele);
            //visited[ele] = 1;
            q.pop();
            for (auto num:adj[ele]){
                if (!visited[num]){
                    q.push(num);
                    visited[num] = 1;
                }
            }
        }
        return ans;
    }
};

